async function main() {
  const restaurants = dedupeByUrl(RESTAURANT_SEEDS.map(toRestaurant));
  const nonRestaurants = dedupeByUrl(NON_RESTAURANT_SEEDS);

  const dataset: ValidationDataset = {
    restaurants,
    non_restaurants: nonRestaurants,
    metadata: {
      total_restaurants: restaurants.length,
      total_non_restaurants: nonRestaurants.length,
      built_at: new Date().toISOString(),
    },
  };

  await mkdir(path.dirname(OUTPUT_PATH), { recursive: true });
  await writeFile(OUTPUT_PATH, `${JSON.stringify(dataset, null, 2)}\n`, 'utf8');

  const states = new Set(restaurants.map((r) => r.state));
  const cuisines = new Set(restaurants.map((r) => r.cuisine));
  const categories = new Set(nonRestaurants.map((n) => n.category));

  console.log(`Wrote ${OUTPUT_PATH}`);
  console.log(`  restaurants: ${restaurants.length} across ${states.size} states, ${cuisines.size} cuisine types`);
  console.log(`  non-restaurants: ${nonRestaurants.length} across ${categories.size} categories`);
}

function dedupeByUrl<T extends { url: string }>(items: T[]): T[] {
  const seen = new Set<string>();
  return items.filter((item) => {
    const key = item.url.toLowerCase().replace(/\/$/, '');
    if (seen.has(key)) return false;
    seen.add(key);
    return true;
  });
}

main().catch((error) => {
  console.error(error);
  process.exitCode = 1;
});
