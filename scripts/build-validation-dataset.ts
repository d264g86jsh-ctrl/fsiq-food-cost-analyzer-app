// ── Compact tuple helpers ────────────────────────────────────────────────────
// [domain, name, city, state, cuisine]
type RTuple = [string, string, string, string, string];

function toRestaurant(t: RTuple): RestaurantEntry {
  return {
    url: t[0].startsWith('http') ? t[0] : `https://${t[0]}`,
    name: t[1],
    city: t[2],
    state: t[3],
    cuisine: t[4],
    source: 'seed',
  };
}

function nr(url: string, name: string, category: string): NonRestaurantEntry {
  return {
    url: url.startsWith('http') ? url : `https://${url}`,
    name,
    category,
    source: 'seed',
  };
}

