// ── Types ────────────────────────────────────────────────────────────────────

interface RestaurantEntry {
  url: string;
  name: string;
  city: string;
  state: string;
  cuisine: string;
  source: string;
}

interface NonRestaurantEntry {
  url: string;
  name: string;
  category: string;
  source: string;
}

interface ValidationDataset {
  restaurants: RestaurantEntry[];
  non_restaurants: NonRestaurantEntry[];
  metadata: {
    total_restaurants: number;
    total_non_restaurants: number;
    built_at: string;
  };
}

